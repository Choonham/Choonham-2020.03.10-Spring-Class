package com.choonham;

public class BookClass {
	
	String title, author, press, image;
	int price, dc;
	public BookClass() {
			
	}
	
	public BookClass(String title, String author, String press, int price, String image, int dc) {
		this.title = title;
		this.author = author;
		this.press = press;
		this.price = price;
		this.image = image;
		this.dc = dc;
		
	}

}
